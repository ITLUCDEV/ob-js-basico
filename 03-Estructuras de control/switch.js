// Sentencias switch
let nota = 5;

switch(nota){
    case 5:
        console.log("Sobresaliente")
        break;
    case 4:
        console.log("Notable")
        break;
    case 3: 
        console.log("Bien")
        break;
    case 2:
        console.log("Suficiente")
        break;
    case 1:
        console.log("Insuficiente")
        break;
    default:
        console.log("Error, introduce una nota entre el 1 y el 5.")
        break;
}
